#include <iostream>
#include <string>
#include <cmath>
#include <vector>
using namespace std;

int main()
{
    cin.tie(NULL);
    cin.sync_with_stdio(false);

    int T;
    cin >> T;

    for (int i = 0; i < T; i++)
    {
        int k, point = 0, ans = 0;
        double x, y;

        cin >> x >> y;

        k = max(x, y) - min(x, y);
        point = ceil(sqrt(k));
        if (point * point - k < point)
        {
            cout << point * 2 - 1;
        }
        else
        {
            cout << point * 2 - 2;
        }
        cout << "\n";
    }

    return 0;
}

